void pr(vector<T> x)
{
    for (auto a : x)
    {
        cout << a << " ";
    }
    cout << endl;
}

class Solution
{
public:
    int lengthOfLIS(vector<int> &nums)
    {
        int n = nums.size();
        vector<int> dp(n, 1);
        if (n == 0)
            return 0;
        for (int i{}; i < n; ++i)
        {
            for (int j{}; j < i; ++j)
            {
                if (nums[j] < nums[i])
                {
                    dp[i] = max(dp[i], dp[j] + 1);
                }
            }
        }
        int ans{INT_MIN};
        for (auto &x : dp)
        {
            ans = max(ans, x);
        }
        return ans;
    }
};

