template <class T>
T upper_divide(T a, T b)
{
    return (a - 1) / b + 1;
}

class MyCalendarThree
{
public:
    map<int, int> record;
    MyCalendarThree()
    {
    }

    int book(int start, int end)
    {
        record[start]++;
        record[end]--;
        int ans = 0, concurrent = 0;
        for (auto &p : record)
        {
            concurrent += p.second;
            ans = max(ans, concurrent);
        }
        return ans;
    }
};

