template <typename T>
void pr(vector<T> x)
{
    for (auto a : x)
    {
        cout << a << " ";
    }
    cout << endl;
}

template <class T>
void pr(vector<vector<T>> x)
{
    for (auto &a : x)
    {
        for (auto &b : a)
        {
            cout << b << " ";
        }
        cout << endl;
    }
    cout << endl;
}

template <class T>
T upper_divide(T a, T b)
{
    return (a - 1) / b + 1;
}

class Solution
{
public:
    int minimumNumbers(int num, int k)
    {
        if (num == 0)
            return 0;
        for (int i = 1; i <= num; ++i)
        {
            if (num - i * k < 0)
                return -1;
            if ((num - i * k) % 10 == 0)
                return i;
        }
    }
};

