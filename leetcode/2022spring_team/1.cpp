template <typename T>
void pr(vector<T> x)
{
    for (auto a : x)
    {
        cout << a << " ";
    }
    cout << endl;
}

template <class T>
void pr(vector<vector<T>> x)
{
    for (auto &a : x)
    {
        for (auto &b : a)
        {
            cout << b << " ";
        }
        cout << endl;
    }
    cout << endl;
}

template <class T>
T upper_divide(T a, T b)
{
    return (a - 1) / b + 1;
}

class Solution
{
public:
    int getMinimumTime(vector<int> &time, vector<vector<int>> &fruits, int limit)
    {
        int ans{};
        for (auto &x : fruits)
        {
            int type = x[0];
            int num = x[1];
            int q = upper_divide(num, limit);
            ans += q * time[type];
        }
        return ans;
    }
};

